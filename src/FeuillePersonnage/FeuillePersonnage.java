package FeuillePersonnage;

public class FeuillePersonnage {
	idPersonnage idPersonnage;
	StatBase statBase;
	Modificateurs modificateurs;	
	
	public idPersonnage getIdPersonnage() {
		return idPersonnage;
	}

	public void setIdPersonnage(idPersonnage idPersonnage) {
		this.idPersonnage = idPersonnage;
	}

	public StatBase getStatBase() {
		return statBase;
	}

	public void setStatBase(StatBase statBase) {
		this.statBase = statBase;
	}

	public Modificateurs getModificateurs() {
		return modificateurs;
	}

	public void setModificateurs(Modificateurs modificateurs) {
		this.modificateurs = modificateurs;
	}
	
	
	
}
